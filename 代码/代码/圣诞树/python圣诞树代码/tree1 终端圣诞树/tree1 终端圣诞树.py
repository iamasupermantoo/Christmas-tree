"""
2021.12.22 简版圣诞树
               ^
              ***
             *****
            $*****$
           *********
          ***********
         $***********$
        ***************
       *****************
      $*****************$
     *********************
    ***********************
   $***********************$
  ***************************
 *****************************
             |||||
             |||||
             |||||
"""

stars = 1
width = 15
tree = 3

for i in range(width):
    if i == 0:
        print((' ' * (width - i)) + ('^' * stars))
    elif i % 3 == 0:
        print((' ' * (width - i)) + '$' + ('*' * (stars - 2)) + '$')
    else:
        print((' ' * (width - i)) + ('*' * stars))
    stars += 2

for i in range(tree):
    print((' ' * (width - 2)) + '|' * 5)
